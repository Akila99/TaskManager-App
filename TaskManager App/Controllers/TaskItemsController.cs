using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using TaskManager_App.Data;
using TaskManager_App.Models;

namespace TaskManager_App.Controllers
{
    public class TaskItemsController : Controller
    {
        private readonly TaskManager_AppContext _context;

        public TaskItemsController(TaskManager_AppContext context)
        {
            _context = context;
        }

        public async Task<IActionResult> Index(string searchString)
        {
            if (_context.TaskItem == null)
            {
                return Problem("Entity set 'Taskmanager.Task'  is null.");
            }

            var tasks = from m in _context.TaskItem
                        select m;

            if (!String.IsNullOrEmpty(searchString))
            {
                tasks = tasks.Where(s => s.Title!.ToUpper().Contains(searchString.ToUpper()));
            }

            return View(await tasks.ToListAsync());
        }

        [HttpPost]
        public string Index(string searchString, bool notUsed)
        {
            return "From [HttpPost]Index: filter on " + searchString;
        }

        //// GET: TaskItems
